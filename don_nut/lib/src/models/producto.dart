class Producto {
  int idProducto;
  String nombre;
  String tipo;
  String? imgBanner;
  String imgProducto;
  String descripcion;
  String? fechaRegistro;
  String precio;
  String? estado;

  Producto(
      this.idProducto,
      this.nombre,
      this.tipo,
      this.imgBanner,
      this.imgProducto,
      this.descripcion,
      this.fechaRegistro,
      this.precio,
      this.estado);
}
